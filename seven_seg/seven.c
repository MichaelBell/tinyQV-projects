#include <gpio.h>
#include <csr.h>

uint32_t led_data[] = {0x7f, 0x3f, 0x1f, 0xf, 0x7, 0x3, 0x1, 0, 0x1, 0x2, 0x4, 0x8, 0x10, 0x20, 0x40, 0x80, 0x1, 0x3, 0x7, 0xf, 0x1f, 0x3f, 0x7f, 0xff};

int main(void)
{
    set_gpio_sel(0xff);
    int i = 0;
    while (1) {
        set_outputs(led_data[i++]);
        if (i == sizeof(led_data) / sizeof(led_data[0])) i = 0;

        delay_us(50000);
    }
}
